        private readonly Dictionary<char, char> _pairs = new Dictionary<char, char> { { '(', ')' }, { '<', '>' }, { '[', ']' }, { '{', '}' } };
        internal void Part1()
        {
            var scores = new Dictionary<char, int> { { ')', 3 }, { ']', 57 }, { '}', 1197 }, { '>', 25137 } };
            int score = 0;
            foreach (var line in _lines)
            {
                var stack = new Stack<char>();
                foreach (var c in line)
                {
                    if (_pairs.Keys.Contains(c))
                    {
                        stack.Push(c);
                    }
                    else
                    {
                        if (stack.TryPop(out var s))
                        {
                            if (c != _pairs[s])
                            {
                                // corrupted
                                score += scores[c];
                                break;
                            }
                        }
                    }
                }
            }
            System.Console.WriteLine(score);
        }

        internal void Part2()
        {
            var scores = new Dictionary<char, int> { { ')', 1 }, { ']', 2 }, { '}', 3 }, { '>', 4 } };
            var incompleteScores = new List<long>();
            foreach (var line in _lines)
            {
                if (!LineIsCorrupted(line))
                {
                    var stack = new Stack<char>();
                    foreach (var c in line)
                    {
                        if (_pairs.Keys.Contains(c))
                        {
                            stack.Push(c);
                        }
                        else
                        {
                            stack.Pop();
                        }
                    }
                    // incomplete
                    long score = 0;
                    while (stack.TryPop(out var c))
                    {
                        score = score * 5 + scores[_pairs[c]];
                    }
                    incompleteScores.Add(score);
                }
            }

            var sortedScores = incompleteScores.OrderBy(s => s).ToArray();
            var middleScore = sortedScores[(sortedScores.Count() - 1) / 2];

            System.Console.WriteLine(middleScore);
        }

        private bool LineIsCorrupted(string line)
        {
            var stack = new Stack<char>();
            foreach (var c in line)
            {
                if (_pairs.Keys.Contains(c))
                {
                    stack.Push(c);
                }
                else
                {
                    if (stack.TryPop(out var s))
                    {
                        if (c != _pairs[s])
                        {
                            return true;
                        }
                    }
                }
            }
            return false;
        }

